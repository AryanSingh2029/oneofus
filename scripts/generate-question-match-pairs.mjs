const pairs = categories.flatMap((category) =>
  category.contexts.flatMap((context) =>
    category.actions.map(([civilianAction, impostorAction]) => ({
      category: category.key,
      categoryLabel: category.label,
      civilian: `Who is most likely to ${civilianAction} ${context}?`,
      impostor: `Who is most likely to ${impostorAction} ${context}?`,
    })),
  ),
);

if (pairs.length !== 2000) {
  throw new Error(`Expected 2000 pairs, generated ${pairs.length}.`);
}

const jsonPairs = pairs.map((pair, index) => ({
  id: `question-${String(index + 1).padStart(4, "0")}`,
  ...pair,
}));

const reviewLines = [
  "# Question Match Pair Review",
  "",
  "Format: Civilian question / Impostor question",
  "",
  ...jsonPairs.map(
    (pair, index) =>
      `${String(index + 1).padStart(4, "0")}. ${pair.civilian} / ${pair.impostor}`,
  ),
];

writeFileSync(
  "src/lib/content/question-match-pairs.json",
  `${JSON.stringify(jsonPairs, null, 2)}\n`,
);
writeFileSync(
  "src/lib/content/question-match-pairs-review.md",
  `${reviewLines.join("\n")}\n`,
);

